                </li>
                <li>
                    کاربران متعهد می‌شوند در حین استفاده از خدمات آو قوانین جاری بر اپلیکیشن را رعایت نمایند
                </li>
                <li>
                    با استفاده از خدمات آو کاربران می‌پذیرند و موظف می‌باشند که از آو برای انجام هیچ فعالیت غیرقانونی طبق قوانین جمهوری اسلامی ایران یا خلاف شرع یا مخالف با عرف جامعه استفاده نکنند.
                </li>
                <li>
                    کاربران متعهد می‌شوند هیچ‌گاه از خدمات آو به صورتی که به آو صدمه‌ای وارد کند استفاده نکنند. در صورتی که کاربران نسبت به سوءاستفاده از امکانات فراهم شده در خدمات آو در جهت منافع شخصی و یا آسیب اعم از مادی و یا معنوی به منافع موسسه اقدام نمایند، موسسه ضمن داشتن حق قطع خدمات و غیرفعال نمودن حساب کاربری، مجاز خواهد بود با مراجعه به مراجع قضایی و یا سایر مراجع ذیصلاح قانونی نسبت به مطالبه جبران خسارات وارده اقدام نماید.ملاک تشخیص صدمه و آسیب با موسسه می باشد.
                </li>
                <li>
                    کاربران می‌پذیرند که موسسه ممکن است از ارسال پیامک، ایمیل و یا Push Notification به عنوان راه ارتباطی موسسه با وی استفاده کند. کاربران می‌توانند درخواست کنند که ارسال این پیام‌ها قطع شود اما می‌پذیرند که با انصراف از دریافت این پیام‌ها، ممکن است در استفاده از خدمات با مشکل مواجه شوند و یا اطلاعات مقتضی به موقع به ایشان نرسد
                </li>
                <li>
                    مسئولیت تامین اینترنت و سخت‌افزار لازم و همچنین پرداخت هزینه‌های مربوط به آنها برای استفاده از خدمات آو به عهده کاربران است
                </li>
