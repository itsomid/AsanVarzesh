                </li>
                <li>
                    با استفاده از خدمات آو کاربران می‌پذیرند که در زمان دوره تمرینی ، آو را وسیله‌ای برای تبلیغات و بازاریابی کالا و خدمات شخصی خود قرار ندهند و از هرگونه ایجاد روابط کاری و تجاری با هنرجویان و کارشناسان و متخصصان و معرفی و عرضۀ محصولات و خدمات شخصی و یا متعلق به دیگران خودداری کنند.
                </li>
                <li>
                    کاربران متعهد به رعایت همۀ قوانین اسلامی، شرعی، اخلاقی و اجتماعی جمهوری اسلامی ایران از جمله رعایت حجاب مناسب در هنگام استفاده از آو هستند.
                </li>
                <li>
                    کاربران می‌پذیرند و اعلام می‌دارند که متن حریم خصوصی آو را مطالعه کرده و مورد قبول ایشان است.
                </li>
                <li>
                    کاربران می‌پذیرند که آو خدمات باشگاه وبه طور کلی ارائه دهنده خدمات ورزشی، تغذیه ای و سلامتی  نیست بلکه تنها خدمات نرم‌افزاری است که ارتباط بین هنرجو (ورزشکار)ان و مربیان را جهت توافق بر انجام یک دوره ورزشی فراهم می‌کند. در خدمات آو کاربر مربی مختار است یک درخواست پرونده را بپذیرد یا رد کند. لذا هر دوره ای که هنرجو (ورزشکار) با مربی انجام دارد باید به عنوان یک قرارداد لازم مستقل و رابطۀ قراردادی بین مربی و هنرجو (ورزشکار) در نظر گرفته شود؛در نتیجه آو مسئولیتی در قبال نوع و کیفیت ارائه دوره ها، برنامه ها و عملکرد مربیان ندارد.
                </li>
                <li>
                    خدمات آو ممکن است با مشارکت خدمات موسسه‌های دیگری ارائه شود که موسسه آو کنترلی روی آنها ندارد و زیرمجموعه ی موسسه نیز به شمار نمی آیند. در چنین شرایطی کاربران می‌پذیرند که این خدمات، قوانین استفاده و مقررات مختص خود را دارند و آو مسئولیتی در خصوص قوانین و خدمات این موسسه‌ها ندارد. همچنین نسبت به هر نوع لینک ارتباطی داخل اپلیکیشن آو بین آو و موسسات تبلیغی و خدماتی، موسسه آو هیچگونه مسئولیتی در قبال ارائه خدمات آنها ندارد.
                </li>
                <li>
                    مربیان آو متعهد هستند صرفا از طریق اپلیکیشن آو با کاربران هنرجو ارتباط برقرار نمایند و در صورت سو استفاده از این بخش و پیشنهادهای دیگر و دعوت کاربر هنرجو به استفاده از سایر اپلیکیشن های هم موضوع با آو، موسسه این حق را دارد تا ضمن بستن حساب کاربری مربی نسبت به شکایت علیه او در مراجع قضایی اقدام نماید.
                </li>
            </ul>
            <span class="article">ماده 4- هزینه ها و پرداخت:</span>
            <ul>
                <li>
                    مربی می بایست ضمن ثبت نام در آو شماره حساب بانک ........  خود را به موسسه جهت پرداخت حق الزحمه اعلام نماید.
                </li>
                <li>
                    از میزان وجه دریافتی از سوی کاربر هنرجو(ورزشکار) 10% سهم موسسه و فدراسیون، 10% سهم کارشناس تغذیه، 10% سهم متخصص حرکتهای اصلاحی و 70% سهم مربی است که در پایان هر ماه محاسبه و به شماره حساب وی واریز می گردد.
                </li>
            </ul>
            <span class="article">ماده 5- مسئولیت آو:</span>
            <ul>
                <li>
                    موسسه با تمام امکانات خود از جمله مرکز تماس ۲۴ ساعته، واحد رسیدگی به شکایات و فرآیند میانجیگری تمامی تلاش خود را جهت رضایت کاربران، رفع اختلافات ایجاد شده در زمان ارائه و استفاده از خدمات آو بین کاربران، و ایجاد صلح و سازش فی مابین آنها می‌کند
                </li>
                <li>
                    موسسه تلاش خود را جهت فراهم نمودن امکان استفاده از خدمات نرم‌افزاری خود توسط دستگاه‌های کاربران محترم می‌کند اما تضمینی نسبت به اجرا شدن آن روی همۀ دستگاه‌ها یا همۀ شبکه‌های ارتباطی (اینترنت، شبکۀ تلفن‌ همراه و …) نخواهد داد
                </li>
                <li>
                    تمامی اطلاعات کاربران به عنوان اطلاعات محرمانه نزد موسسه محافظت شده و دسترسی به آن توسط اشخاص ثالث ممنوع بوده مگر برابر تصمیم مقام ذیصلاح قضایی. بنابراین در صورت درخواست و صدور دستور مقتضی از سوی مراجع قضایی، موسسه مکلف به ارائه اطلاعات کاربران به مراجع مذکور می‌باشد
                </li>
                <li>
                    چنانچه هر یک از کاربران آو در زمان استفاده از خدمات آن با مشکلی مواجه شوند برابر شرایط فوق می‌توانند شکایت خود را از طریق مرکز تماس ۲۴ ساعته و واحد رسیدگی به شکایات آو مطرح و موسسه پیگیری لازم و تلاش خود را جهت حل و فصل اختلافات بعمل خواهد آورد
                </li>
                <li>
                    جهت اطلاع بیشتر کاربران با نحوه و شرایط ارائه خدمات آو و استفاده از نرم‌افزار‌ مربی، همۀ اطلاعات مورد نیاز کاربران توسط آو در نرم‌افزار قرار داده شده است و اشخاص می‌توانند با مراجعه به قسمت مذکور و مطالعۀ آنها کاملا با شرایط ذکر شده و خدمات آو آشنا شوند. همچنین همکاران ما در مرکز تماس به صورت ۲۴ ساعته پاسخگوی هر گونه ابهام و سوال کاربران هستند. لازم به توضیح است که ارائه اطلاعات کاربری صرفا از طریق اپلیکیشن امکان پذیر است و به منظور حفظ امنیت اطلاعات کاربران و اصل محرمانگی ارائه اطلاعات کاربری به خود کاربر و اشخاص ثالث به صورت تلفنی و حضوری مقدور نیست
                </li>
                <li>
                    آو مسئولیتی نسبت به روابط خارج از اپلیکیشن میان مربی و هنرجو(ورزشکار) ندارد. تنها مسئولیت آو نسبت به ارائه خدمات نرم افزاری بر روی بستر اپلیکیشن می باشد.
                </li>
            </ul>
            <span class="article">ماده 6- تعهدات مربی:</span>
            <ul>
                <li>
                    مربیان موظفند پس از ثبت نام در آو نسبت به ارسال مدرک مربیگری خود و سایر موارد درخواستی اقدام کنند. آو پس از بررسی و استعلام از سوی فدراسیون و در صورت تایید، با فرستادن پیامی مربی مورد نظر را مطلع می سازد. بدیهی است پیش از تایید مدارک مربی از سوی آو، هیچ تعهداتی از سوی موسسه نسبت به مربی وجود ندارد و صرف ثبت نام به منزله ایجاد تعهد از سوی موسسه نیست
                </li>
                <li>
                    مربی موظف به رعایت کلیه شئونات حرفه ای و اخلاقی مطابق با نظر و صلاحدید موسسه می باشد و از هر نوع اهمالکاری(از جمله برنامه ندادن یا عدم ارائه برنامه مناسب و کافی)، سوء استفاده عاطفی(از جمله سرزنش هنرجو یا بدرفتاری با وی)، سوء استفاده شغلی(از جمله ایجاد روابط خارج از عرف کاری و اخلاق، برقراری روابط تجاری و مالی، دخالت در امور سایر کارشناسان و متخصصان) خودداری می کند.
                </li>
                <li>
                    مربی موظف است پس از قبول درخواست از طرف هنرجو به ارائه برنامه، پیگیریهای لازم و پرسش و پاسخ جهت کسب اطلاعات لازم از فرایند ارتقای هنرجو مطابق با امکانات اپلیکیشن و صرفا در بستر آو اقدام کند. ارائه برنامه صرفا مربوط به حوزه ورزشی می باشد و مربی حق ارائه برنامه غذایی(از جمله قرصها و مکملهای غذایی) یا پیشنهاد آن را به هنرجو ندارد و صرفا می تواند در صورت تمایل، به کارشناس تغذیه پیشنهاد آن را ابراز کند.
                </li>
                <li>
                    مربی صرفا ازاطلاعاتی که در چارچوب فضای اپلیکیشن و پرونده ی هنرجو می باشد، جهت ارائه برنامه و پیگیری لازم اقدام می کند و حق درخواست ارسال اطلاعاتی خارج از پرونده ی هنرجو(از جمله درخواست ارسال عکس از بدن) را ندارد.
                </li>
                <li>
                    ممکن است اجرای برخی برنامه های ارائه شده از سوی مربی با نظرات و صلاحدید متخصص حرکتهای اصلاحی در ارتباط با هنرجو تضاد داشته باشد، با اعلام موضوع از سوی هنرجو به موسسه، موسسه نسبت به رفع تضاد و تناقض اقدام خواهد کرد. در غیر اینصورت موسسه پاسخگوی خسارات احتمالی نیست.
                </li>
                <li>
                    مربیان هیچ رابطه استخدامی با آو ندارند و کارمند آو نمی باشند همچنین موسسه مدیر مربیان و کارفرمای آنها قلمداد نمی شود در نتیجه تمام مسئولیتهای قانونی در ارتباط با هنرجو(ورزشکار)ها با مربیان است و رابطه مربی و هنرجو به محض توافق طرفین در بستر اپلیکیشن، یک رابطه دوجانبه مستقل تلقی می گردد.
                </li>
                <li>
                    مسئولیت همۀ اقدامات و افعال کاربران که ناشی از عدم رعایت قوانین موضوعه جمهوری اسلامی ایران از جمله قانون مجازات اسلامی (ارتکاب جرم در هنگام دوره) و قوانین مدنی (ورود خسارت به اموال و حیثیت کاربران) باشد، برعهدۀ شخص متخلف بوده و موسسه هیچ گونه مسئولیتی در قبال اعمال و افعال فوق نداشته و نخواهد داشت و موسسه می‌تواند ضمن غیر فعال کردن حساب کاربری متخلف، از طریق مراجع ذیصلاح، اقدامات قانونی را علیه نامبرده نسبت به احقاق حقوق قانونی موسسه به عمل آورد
                </li>
                <li>
                    هر گونه اقدامی از سوی کاربر مربی که منجر به ورود خسارت به شهرت، اعتبار و دارایی‌های موسسه شود تخلف از شرایط قراردادی و قانونی تلقی خواهد شد. موسسه این حق را برای خود محفوظ می‌داند که حساب کاربر مربی را مسدود کند و در صورت لزوم، علیه شخص اقدام قانونی انجام دهد و جبران خسارت و حقوق قانونی خود را مطالبه کند.
                </li>
                <li>
                    مربی متعهد می‌شود تنها از اطلاعات، گزارشها، پيامها و هر نوع داده ي سازمانی و اقلامی که توسط آو تهیه و در اختیار او گذاشته شده است استفاده کند و مجاز به تأمین آنها خارج از بستر آو  نیست و مسئولیت استفاده از اطلاعات، اقلام و داده هايي که سبب ایجاد مشابهت و یا ایجاد اشتباه برای کاربران هنرجو (ورزشکار) و یا سایر اشخاص  کند بر عهدۀ  مربی بوده و در صورت لزوم موسسه به قطع خدمات با نامبرده و پیگیری قانونی اقدام خواهد نمود و شخص متخلف مکلف به جبران خسارات وارده به موسسه خواهد بود.
                </li>
                <li>
                    مسئولیت رعایت همۀ قوانین و مقررات و آیین نامه های مرتبط با سازمانها و نهادهایی که در حوزه ورزش، بهداشت و سلامت و حتی تغذیه و قوانین حاکم بر آن فعالیت دارند بر عهده کاربر مربی است
                </li>
                <li>
                    در صورتی که در حین دوره کاربر مربی نسبت به نقض تعهدات قانونی از سوی کاربر هنرجو (ورزشکار) اطلاع پیدا کند، کاربر مربی متعهد به اعلام گزارش به مرکز تماس موسسه خواهد بود.
                </li>
                <li>
                    مربی حق واگذاری تعهدات خود را مطابق با این الزامات و قوانین، جزئاً و یا کلاً به دیگری ندارد و شخصاً موظف به ارائه خدمات مربیگری می باشد.
                </li>
            </ul>
            <span class="article">ماده 7-  مالکیت حقوق معنوی آو:</span>
            <ul>
                <li>
                    مالکیت معنوی آو و تمام متعلقات آن (از جمله نام، علامت تجاری، تکنولوژی و خدمات ارائه‌شده تحت این نام و ...) متعلق به موسسه رایان راحیل ایرانیان است و کاربر جزئاً و کلاً هیچ حقی نسبت به این حقوق ندارد.
                </li>
                <li>
                    کاربران تنها اجازه‌ی استفاده‌ی محدود و غیرانحصاری و غیرقابل انتقال از خدمات آو را تحت شرایط قرارداد حاضر دارند، بدون آن که این استفاده حقی برای آن‌ها نسبت به مالکیت آو یا سایر حقوق معنوی مربوط به آن ایجاد نماید یا اجازه‌ی استفاده از نام، علامت تجاری، لوگو و ... را به آن‌ها اعطا نماید.
                </li>
                <li>
                    کاربران نمی‌توانند اپلیکیشن آو را تغییر دهند، بازتولید یا بازنویسی کنند؛ همچنین نمی‌توانند برنامه را مشابه‌سازی کنند یا بخشی از برنامه را درون برنامه‌ی دیگری استفاده نمایند
                </li>
                <li>
                    کاربران حق ندارند هیچگونه تلاشی در جهت استخراج سورس کد (Source Code) نرم‌افزارهای آو شامل دیکامپایل(Decompile) ، مهندسی معکوس(Reverse Engineering) یا فعالیت‌های مشابه انجام دهد، مگر اینکه اجازۀ چنین کاری به صورت قانونی صادر شده باشد.
                </li>
                <li>
                    کاربران مجاز نیستند با استفاده از روش‌های Framing یا Mirroring یا روش‌های دیگر نسخۀ دیگری از خدمات آو را روی سرورهای دیگر ارائه کنند. همچنین کاربر مجاز نیست در نرم‌افزار آو یا در روش ارائۀ خدمات تغییر یا اخلالی ایجاد کند.
                </li>
                <li>
                    کاربران حق ندارند نرم‌افزار یا اسکریپتی با هدف ایندکس گذاری (Indexing)، مطالعه یا هر گونه فعالیت داده‌کاوی روی خدمات اجرا کنند.
                </li>
                <li>
                    کاربران حق ندارند هیچ فعالیتی در راستای حصول دسترسی غیرقانونی و غیر متداول به هیچ بخش از سامانۀ آو یا سامانه‌های مرتبط با آو انجام دهند.
                </li>
                <li>
                    موسسه ممکن است در شرایطی به کاربران اجازه دهد که در وب سایت، نرم‌افزار موبایل و یا سایر بخش‌های این سامانه، عکس، متن، فایل صوتی، فایل تصویری یا فایل‌های مشابه منتشر کنند. مالکیت هر مطلب (شامل متن، عکس، فایل صوتی، فایل تصویری و …) که به این شکل در خدمات ثبت شود همچنان متعلق به کاربران خواهد بود. اما با ثبت آنها در خدمات‌ مرتبط به آو کاربران این حق را به آو می‌دهند که آنها را در هر مکان و هر زمانی به صورت دیجیتال یا چاپی برای هر نوع کاربردی منتشر کند. همچنین موسسه مجاز است حق انتشار این مطالب را به اشخاص حقیقی یا حقوقی دیگر منتقل کند.
                </li>
            </ul>
            <span class="article">ماده 8- محرمانگی و عدم افشاء اطلاعات:</span>
            <ul>
                <li>
                    مربیان متعهد به محرمانه نگه داشتن کلیه اطلاعات دریافتی از سوی آو اعم از تمام اطلاعات موجود در پرونده هنرجوها  خواهند بود و آنها را افشا و منتشر نخواهند کرد و از این دسته از اطلاعات بهره‌برداری و استفاده خارج از بستر آو نمی‌نمایند. مربیان تمام اطلاعات محرمانه را به یک‌ اندازه محرمانه تلقی خواهند نمود و در این زمینه  تمام اقدامات متعارفی که باید برای حفظ اطلاعات محرمانه صورت گیرد، اتخاذ می‌نمایند
                </li>
                <li>
                    ازآنجایی که مربیان متعهد به حفظ و نگهداری و عدم افشای اطلاعات هنرجوها هستند. بدیهی است در صورت بروز هرگونه خسارت احتمالی ناشی از افشای اطلاعات هنرجو، مربی مسئول پاسخگویی و جبران خسارت(مادی یا معنوی) خواهد بود
                </li>
            </ul>
            <span class="article">ماده 9- رویدادهای خارج از کنترل:</span>
            <ul>
                <li>
                    در صورت بروز رویدادهای خارج از کنترل موسسه( از جمله اختلال در شبکه اینترنت) که موسسه تسلطی بر آن ندارد، مسئولیتی بابت تأخیر یا عدم ارائه خدمات در دوره مذکور بر عهده موسسه نیست.
                </li>
                <li>
                    موسسه می تواند در صورت رفع موانع خارج از کنترل و توقف ایجاد شده در ارائه خدمات، بر مدت ارائه خدمات بیفزاید و خدماتی جهت بهبود وضع صورت گرفته انجام دهد.
                </li>
            </ul>
            <span class="article">ماده 10- قطع خدمات:</span>
            <ul>
                <li>
                    موسسه در شرایطی که به هر دلیل به این نتیجه برسد حضور یک کاربر  می‌تواند برای موسسه یا سایر هنرجو (ورزشکار)ان یا کارشناسان خطرناک باشد یا مطلوب نباشد، طبق صلاحدید و تشخیص خود حق حذف دسترسی کاربر مذکور به خدمات آو را دارد. در این صورت، کاربر حق طرح هیچگونه ادعا و یا اعتراضی را نخواهد داشت.
                </li>
                <li>
                    در صورت عدم همکاری کاربر و ارائه نکردن اطلاعات مربوط - در شرایط خاص که ممکن است احراز هویت کاربر از طرف موسسه ضروری تشخیص داده شود - شرکت حق انسداد حساب کاربر را خواهد داشت.
                </li>
            </ul>
            <span class="article">ماده 11- توافقات:</span>
