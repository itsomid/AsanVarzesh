                        <br>5.	چنانچه هر یک از کاربران آو در زمان استفاده از خدمات آن با مشکلی مواجه شوند برابر شرایط فوق می‌توانند شکایت خود را از طریق مرکز تماس ۲۴ ساعته و واحد رسیدگی به شکایات آو مطرح و موسسه پیگیری لازم و تلاش خود را جهت حل و فصل اختلافات بعمل خواهد آورد.

                        <br>6.	جهت اطلاع بیشتر کاربران با نحوه و شرایط ارائه خدمات آو و استفاده از نرم‌افزار‌های هنرجو (ورزشکار) و مربی، همۀ اطلاعات مورد نیاز کاربران توسط آو در نرم‌افزار قرار داده شده است و اشخاص می‌توانند با مراجعه به قسمت مذکور و مطالعۀ آنها کاملا با شرایط ذکر شده و خدمات آو آشنا شوند. همچنین همکاران ما در مرکز تماس به صورت ۲۴ ساعته پاسخگوی هر گونه ابهام و سوال کاربران هستند. لازم به توضیح است که ارائه اطلاعات کاربری صرفا از طریق اپلیکیشن امکان پذیر است و به منظور حفظ امنیت اطلاعات کاربران و اصل محرمانگی ارائه اطلاعات کاربری به خود کاربر و اشخاص ثالث به صورت تلفنی و حضوری مقدور نیست.

                        <br>7.	مسئولیت آو فقط در فرضی است که کاربر هنرجو (ورزشکار) از طریق اپلیکیشن آو درخواست دوره ارسال کرده است و کاربر مربی نیز این درخواست را پذیرفته است. لذا اگر کاربر مربی و کاربر هنرجو (ورزشکار) که در اپلیکیشن آو ثبت نام کرده‌اند خارج از اپلیکیشن با یکدیگر بر انجام دوره توافق نمایند هیچ گونه مسئولیتی متوجه موسسه نخواهد بود.

                        <br><br>ماده ۶ - مسئولیت کاربران<br>

                        <br>	1.	مسئولیت همۀ اقدامات و افعال کاربران که ناشی از عدم رعایت قوانین موضوعه جمهوری اسلامی ایران از جمله قانون مجازات اسلامی (ارتکاب جرم در هنگام دوره) و قوانین مدنی (ورود خسارت به اموال کاربران) باشد، برعهدۀ شخص متخلف بوده و موسسه هیچ گونه مسئولیتی در قبال اعمال و افعال فوق نداشته و نخواهد داشت و موسسه می‌تواند ضمن غیر فعال کردن حساب کاربری متخلف، از طریق مراجع ذیصلاح، اقدامات قانونی را علیه نامبرده نسبت به احقاق حقوق قانونی موسسه به عمل آورد.

                        <br>2.	هر گونه اقدامی از سوی کاربر مربی که منجر به ورود خسارت به شهرت، اعتبار و دارایی‌های موسسه شود تخلف از شرایط قراردادی و قانونی تلقی خواهد. موسسه این حق را برای خود محفوظ می‌داند که حساب کاربر مربی را مسدود کند و در صورت نیاز، علیه شخص اقدام قانونی انجام دهد و خسارت و حقوق قانونی خود را مطالبه کند.

                        <br>3.	کاربر مربی متعهد می‌شود تنها از علایم و اقلام سازمانی که توسط آو تهیه و در اختیار او گذاشته شده است استفاده کند و مسئولیت استفاده از اقلام و علایمی که سبب ایجاد مشابهت و یا ایجاد اشتباه برای کاربران هنرجو (ورزشکار) و یا سایر اشخاص اعم از حقیقی کند بر عهدۀ کاربر مربی بوده و در صورت لزوم موسسه به قطع خدمات با نامبرده و پیگیری قانونی اقدام خواهد نمود و شخص متخلف مکلف به جبران خسارات وارده به موسسه خواهد بود.

                        <br>4.	مسئولیت رعایت همۀ قوانین و مقررات و آیین نامه های مرتبط با سازمانها و نهادهایی که در حوزه ورزش، بهداشت و سلامت و حتی تغذیه و قوانین حاکم بر آن بر عهده کاربر مربی است.

                        <br>5.	در صورتی که در حین دوره کاربر مربی نسبت به نقض تعهدات قانونی از سوی کاربر هنرجو (ورزشکار) اطلاع پیدا کند، کاربر مربی متعهد به اعلام گزارش به مرکز تماس موسسه خواهد بود.

                        <br>6.	کاربر هنرجو (ورزشکار) مطلع است و می‌پذیرد که در زمان نصب نرم‌افزار آو یا به‌روزرسانی آن، نسبت به فعال‌سازی یا غیرفعال‌سازی گزینه‌ی دسترسی به «اطلاعات آماری» در بخش تنظیمات نرم‌افزار اقدام می‌کند. کاربر هنرجو (ورزشکار) در فعال‌سازی یا غیرفعال‌سازی گزینه‌ دسترسی به «اطلاعات آماری» کاملا مختار است. لازم به توضیح است که دسترسی به اطلاعات آماری در حوزه وضعیت سلامتی و تمرینات برای بهبود تجربه دوره با آو به کار می‌رود و به صورت خودکار فعال است.

                        <br><br>ماده 7 - موارد فنی<br>

                        <br>	1.	کاربران حق ندارند هیچگونه تلاشی در جهت استخراج سورس کد (Source Code) نرم‌افزارهای آو شامل دیکامپایل(Decompile) ، مهندسی معکوس(Reverse Engineering) یا فعالیت‌های مشابه انجام دهد، مگر اینکه اجازۀ چنین کاری به صورت قانونی صادر شده باشد.

                        <br>	2.	کاربران مجاز نیستند با استفاده از روش‌های Framing یا Mirroring یا روش‌های دیگر نسخۀ دیگری از خدمات آو را روی سرورهای دیگر ارائه کنند. همچنین کاربر مجاز نیست در نرم‌افزار آو یا در روش ارائۀ خدمات تغییر یا اخلالی ایجاد کند.

                        <br>3.	کاربران حق ندارند نرم‌افزار یا اسکریپتی با هدف ایندکس گذاری (Indexing)، مطالعه یا هر گونه فعالیت داده‌کاوی روی خدمات اجرا کنند.

                        <br>4.	کاربران حق ندارند هیچ فعالیتی در راستای حصول دسترسی غیرقانونی و غیر متداول به هیچ بخش از سامانۀ آو یا سامانه‌های مرتبط با آو انجام دهند.
                        <br><br>ماده 8 - محتوای تولید شده توسط کاربران<br>

                        <br>1.	موسسه ممکن است در شرایطی به کاربران اجازه دهد که در وب سایت، نرم‌افزار موبایل و یا سایر بخش‌های این سامانه، عکس، متن، فایل صوتی، فایل تصویری یا فایل‌های مشابه منتشر کنند. مالکیت هر مطلب (شامل متن، عکس، فایل صوتی، فایل تصویری و …) که به این شکل در خدمات ثبت شود همچنان متعلق به کاربران خواهد بود. اما با ثبت آنها در خدمات‌ مرتبط به آو کاربران این حق را به آو می‌دهند که آنها را در هر مکان و هر زمانی به صورت دیجیتال یا چاپی برای هر نوع کاربردی منتشر کند. همچنین موسسه مجاز است حق انتشار این مطالب را به اشخاص حقیقی یا حقوقی دیگر منتقل کند.

                        <br><br>ماده 9 - قطع خدمات<br>

                        <br>1.	موسسه در شرایطی که به هر دلیل به این نتیجه برسد حضور یک کاربر (مربی یا هنرجو (ورزشکار)) می‌تواند برای موسسه یا سایر هنرجو (ورزشکار)ان یا کارشناسان خطرناک باشد یا مطلوب نباشد، طبق صلاحدید و تشخیص خود حق حذف دسترسی کاربر مذکور به خدمات آو را دارد. در این صورت، کاربر حق طرح هیچگونه ادعا و یا اعتراضی را نخواهد داشت.
                        <br><br>2.	ماده 10- توافق از راه دور<br>

                        <br>3.	کاربران توافق می‌کنند جهت مبادلۀ آسان اطلاعات، همۀ تغییرات و الحاقیه‌های قوانین و مقررات حاضر اعم از تغییر و اضافه کردن شروط آن، ارسال اخطاریه و ابلاغیه‌های مربوط به موسسه، از طریق سامانه آو بعمل آید و کاربران ضمن پذیرش اطلاع از شرط و روند اجرایی آن موافقت خود را نسبت به اعمال شرط مذکور اعلام می‌کنند.

                        <br>4.	مسئولیت حفظ و حراست از حساب کاربران برعهدۀ کاربر مذکور بوده و وی ضمن پذیرش این مسئولیت، همۀ داده پیام‌های صادره از طریق حساب کاربری مذکور را منتسب به خود دانسته و هرگونه ادعایی نسبت به انکار، تردید و جعل داده پیام‌های ارسال شده از ناحیۀ نامبرده غیر قابل استماع و مردود است.

                        <br>5.	کاربران می‌پذیرند هر گونه رویه، اشکال، عبارات مبین تائید و اعلام قبولی و تصدیق داده پیام‌های صادره از طرف موسسه به منزله امضا الکترونیکی و قبولی محسوب می‌شود و هر گونه ادعائی نسبت به امضا و قبولی مذکور غیر قابل استماع و مردود است.

                        <br>6.	کاربران با عضویت در خدمات آو قصد و اراده خود را نسبت به پذیرش انعقاد هرگونه اقدام و عمل حقوقی از جمله عقود و معاملات از راه دور و از طریق سامانۀ رایانه‌ای و الکترونیکی اعلام می‌کنند.

