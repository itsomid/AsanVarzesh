                        2.	آو: خدماتی نرم‌ افزاری که توسط موسسه به منظور خدمات ورزشی، تغذیه ای و سلامتی ارائه می‌شود.<br>

                        3.	اپلیکیشن: نرم افزار متعلق به موسسه که به منظور استفاده از خدمات آو، حق استفادۀ موقت از آن برابر شرایط قراردادی و سند حاضر به کاربران اعطا می‌گردد.<br>

                        4.	کاربر: شخصی حقیقی یا حقوقی است اعم از هنرجو(ورزشکار) یا مربی که از طریق اینترنت به اپلیکیشن آو متصل و از خدمات نرم افزاری آو بهرمند می‌شود.<br>

                        5.	کاربران: در این سند کاربر ورزشکار و مربی ها به همراه هم با نام کاربران استفاده می‌شوند.<br>

