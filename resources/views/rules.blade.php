                        <br>5.	کاربر هنرجو (ورزشکار) می‌پذیرد که باید هزینۀ دوره ورزشی را مستقل از هر گونه مشکلی که ممکن است در برنامۀ آو جهت پرداخت آنلاین به وجود آید به موسسه پرداخت کند. در صورتی که به دلیل مشکلی در برنامۀ آو یا مشکلات ارتباطی از جمله اینترنت و شبکۀ تلفن همراه امکان پرداخت از طریق نرم‌افزار فراهم نشود، کاربر هنرجو (ورزشکار) همچنان موظف به پرداخت هزینۀ دوره خواهد بود. در این شرایط پرداخت هزینه ممکن است با هماهنگی کاربر هنرجو (ورزشکار) با مرکز تماس موسسه آو با روش‌های دیگر صورت پذیرد. در صورتی که کاربر هنرجو (ورزشکار) به هر دلیلی از جمله مشکلات نرم‌افزار پرداخت را انجام نداده باشد، موسسه حق دارد مبلغ سفر را از اعتبار کاربر هنرجو (ورزشکار) در آو کسر کند و در صورت عدم پرداخت حساب کاربری مسدود خواهد شد.

