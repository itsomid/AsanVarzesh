                        7.	کاربر هنرجو (ورزشکار): :شخص حقیقی است که در اپلیکیشن کاربر آو به منظور ارسال درخواست یادگیری حرفه ای ورزش با استفاده از خدمات نرم افزاری و سخت افزاری اپلیکیشن از مربی آنلاین؛ کارشناس تغذیه و متخصص حرکت ها ی اصلاحی  ثبت‌ نام کرده‌اند.<br>

