                        <br>6.	موسسه ممکن است کدهایی به منظور هدیه به کاربر هنرجو (ورزشکار) ارائه کند که با وارد کردن این کدها در نرم‌افزار، یا درصدی از هزینۀ دوره کاسته یا درصدی از هزینۀ دوره پس از آن به کاربر برگردانده ‌شود و یا مبلغ مشخصی به حساب کاربر در اپلیکیشن آو اضافه ‌شود. نحوۀ ارائه این خدمات و تعیین مهلت استفاده از آنها، منوط به تشخیص و صلاحدید موسسه بوده و در صورت عدم تعلق کدهای مذکور، کاربر هنرجو (ورزشکار) حق طرح هیچگونه ادعا و اعتراضی را در این خصوص نخواهد داشت.

                        <br>7.	کاربران می‌پذیرند که اجازه انتقال یا فروش کدها را به اشخاص دیگر ندارند. همچنین کاربران مجاز نیستند این کدها را به صورت عمومی منتشر کنند - حتی اگر موسسه در فضاهای عمومی به نحوی که در دسترس عموم است، این کدها را منتشر کرده باشد - مگر اینکه موسسه رسماً این اجازه را به آنها داده باشد.

                        <br>8.	کاربر هنرجو (ورزشکار) می‌پذیرد اعتباری که در حساب کاربری خود دارد مستقل از روش کسب آن (اگر در نتیجه افزایش اعتبار از طریق درگاه بانک باشد یا در نتیجه وارد کردن کدهای تخفیفی که موسسه در اختیار آنها قرار می‌دهد یا هر روش دیگری) فقط برای استفاده از خدمات آو قابل استفاده است و امکان دریافت این وجه را به صورت نقد ندارد.

                        <br>9.	کاربر هنرجو (ورزشکار) می‌پذیرد کدهایی که جهت افزایش اعتبار یا کاهش هزینه دوره به وی داده می‌شود، ممکن است منقضی شوند. از آنجایی که موسسه برای ارائه این کدها هزینه ای از کاربر هنرجو (ورزشکار) دریافت نمی‌کند، این حق برای موسسه محفوظ است که مبلغ یا درصد تاثیر این کدها را حتی پس از انتشار آنها تغییر دهد یا در صورت لزوم آنها را باطل کند. همچنین درصورتی که موسسه تشخیص دهد که استفاده از این کد به دلیل خطای فنی یا تخلف صورت گرفته و یا غیر قانونی بوده است می‌تواند این کدها یا اعتباری که در نتیجۀ استفاده از آنها به حساب کاربر اضافه شده است را باطل یا حذف کند.

                        <br>10.	مسئولیت تامین اینترنت و سخت‌افزار لازم و همچنین پرداخت هزینه‌های مربوط به آنها برای استفاده از خدمات آو به عهده کاربران است.

                        <br>11.	کاربر هنرجو (ورزشکار) می‌پذیرد که در صورت اینکه جنسیت مونث دارد، صرفا پرونده خود را به مربیان خانم ارائه دهد و خود را از هر گونه ارائه پرونده به مربیان آقا منع نموده و در صورت انجام این مورد تمامی مسئولیت احتمالی آن بر گردن خود می باشد و نمی تواند نسبت به این موضوع شکایتی را به موسسه داشته باشد و یا در مراجع قضایی نسبت به موسسه اقامه دعوی نماید.

                        <br>12.	کاربران مربی متعهد می‌شوند پس از اتمام دوره از هیچ بخش از اطلاعاتی که در نتیجۀ استفاده از خدمات آو، از هنرجو (ورزشکار)ان یا مربیان کسب کرده‌اند، هیچ‌گونه استفاده‌ای نکنند. طبعاً ذخیره کردن اطلاعات هنرجو (ورزشکار)ان یا مربیان در دستگاه تلفن همراه یا به هر شیوۀ دیگر نیز مغایر با قوانین آو خواهد بود. همچنین کاربران متعهد می‌شوند در طول دوره، فقط در شرایط ضروری از اطلاعاتی که از هنرجو (ورزشکار)ان یا مربیان در خدمات آو ارائه شده است استفاده کنند. استفادۀ کاربران از این اطلاعات باید مطابق با عرف جامعه و قوانین جمهوری اسلامی ایران باشد. در هر صورت اطلاعات مذکور به صورت امانت در اختیار کاربران قرار گرفته است و در صورت نقض تعهدات مذکور، موسسه مجاز به پیگیری حقوقی و کیفری موضوع خواهد بود.

                        <br>13.	با استفاده از خدمات آو کاربران می‌پذیرند که در زمان دوره تمرینی ، آو را وسیله‌ای برای تبلیغات و بازاریابی کالا و خدمات شخصی خود قرار ندهند و از هرگونه معرفی و عرضۀ محصولات و خدمات شخصی و یا متعلق به دیگران خودداری کنند.

                        <br>14.	کاربران متعهد به رعایت همۀ قوانین اسلامی، شرعی، اخلاقی و اجتماعی جمهوری اسلامی ایران از جمله رعایت حجاب مناسب در هنگام استفاده از آو هستند.

                        <br>15.	کاربران می‌پذیرند و اعلام می‌دارند که متن حریم خصوصی آو را مطالعه کرده و مورد قبول ایشان است.

                        <br>16.	کاربران می‌پذیرند که آو خدمات باشگاه نیست بلکه تنها خدمات نرم‌افزاری است که ارتباط بین هنرجو (ورزشکار)ان و مربیان را جهت توافق بر انجام یک دوره ورزشی فراهم می‌کند. در خدمات آو کاربر مربی مختار است یک درخواست پرونده را بپذیرد یا رد کند، همچنین کاربر هنرجو (ورزشکار) مختار است پس از ارسال درخواست دوره، مشخص شدن مربی و قبل از آغاز دوره نسبت به تغییر مربی خود اقدام نماید. لذا هر دوره ای که هنرجو (ورزشکار) با مربی انجام دارد باید به عنوان یک قرارداد لازم مستقل و رابطۀ قراردادی بین مربی و هنرجو (ورزشکار) در نظر گرفته شود.

                        <br>17.	خدمات آو ممکن است با مشارکت خدمات موسسه‌های دیگری ارائه شود که موسسه آو کنترلی روی آنها ندارد. در چنین شرایطی کاربران می‌پذیرند که این خدمات، قوانین استفاده و مقررات مختص خود را دارند و آو مسئولیتی در خصوص قوانین و خدمات این موسسه‌ها ندارد.

                        <br>	18.	مربیان آو متعهد هستند صرفا از طریق اپلیکیشن آو با کاربران هنرجو ارتباط برقرار نمایند و در صورت سو استفاده از این بخش و پیشنهادهای دیگر و دعوت کاربر هنرجو به استفاده از سایر اپلیکیشن های هم موضوع با آو، موسسه این حق را دارد تا ضمن بستن حساب کاربری مربی نسبت به شکایت علیه او در مراجع قضایی اقدام نماید.

                        <br>19.	کاربر هنرجو (ورزشکار) متعهد می شود که به هیچ عنوان با حساب کاربری خود برای سایر اشخاص درخواست دوره ارسال ننماید. در غیر اینصورت چنانچه پس از پذیرش درخواست دوره توسط مربی، موسسه یا مربی متوجه موضوع شود می تواند نسبت به لغو درخواست دوره اقدام نماید.

                        <br>20.	کاربر هنرجو (ورزشکار) می پذیرد مربیان خانم آو (ویژه بانوان) فقط به منظور ارائه خدمات به بانوان ایجاد گردیده لذا درخواست دوره توسط آو برای کاربر هنرجو (ورزشکار) آقا ممنوع است. چنانچه پس از پذیرش درخواست دوره توسط مربی و ایجاد هرگونه مزاحمت از سوی کاربر هنرجو برای مربی خانم، همه مسئولیت آن کاملا مرتبط به هنرجو آقا خواهد بود.

