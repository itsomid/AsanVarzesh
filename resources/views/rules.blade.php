                        <br>7.	کاربر هنرجو (ورزشکار) می‌پذیرد هر گونه تخریبی که در باشگاه کاربر مربی در نتیجۀ استفاده از خدمات با حساب کاربری وی ایجاد شود، مسئولیت تامین هزینه‌های مربوط به تعویض، تعمیر و غیره به عهده وی خواهد بود. در چنین مواردی موسسه هیچ گونه دخالتی نسبت به  آو کاربر هنرجو (ورزشکار) نخواهد داشت. همچنین چنان چه کاربر هنرجو (ورزشکار) اقدام به فحاشی، توهین و ضرب و جرح و سایر اقدامات خلاف قانون علیه کاربر مربی نماید حق مراجعه کاربر مربی به مراجع قضایی ذیصلاح محفوظ است. علاوه بر آن موسسه حق مراجعه به مراجع قضایی از جهت لطمه به شهرت و اعتبار تجاری خود را خواهد داشت.

                        <br>	8.	کاربر هنرجو (ورزشکار) می‌پذیرد خدمات آو صرفا معرفی مربی، کارشناس تغذیه، متخصص حرکت های اصلاحی و البته ارائه محتوای ورزشی به کاربر هنرجو (ورزشکار) است و همچنین معرفی کاربر هنرجو و درخواست او به کاربران مربی می باشد

